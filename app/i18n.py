def get_translation(lang: str, key: str) -> str:
    if lang == "ru":
        if key in TRANSLATIONS_RU:
            return TRANSLATIONS_RU[key]
        return TRANSLATIONS_EN.get(key, key)
    if lang == "sv":
        if key in TRANSLATIONS_SV:
            return TRANSLATIONS_SV[key]
        return TRANSLATIONS_EN.get(key, key)
    return TRANSLATIONS_EN.get(key, key)


def make_t(lang: str) -> Callable[[str], str]:
    def t(key: str) -> str:
        return get_translation(lang, key)

    return t

